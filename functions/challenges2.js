//Challenge 6 - Create a function that takes two numbers as a parameters and takes the second one away from the first one.
const takeAway = (num1, num2) => {
    return num1 - num2
}
console.log(takeAway(4, 3))

//Challenge 7 - Create a function that takes two numbers as a parameters and multiplies them together.
const multiplyTogether = (num1, num2) => {
    return num1 * num2
}
console.log(multiplyTogether(4, 3))

//Challenge 8 - Create a function that takes two numbers as a parameters and divides the first one by the second one.
const divide = (num1, num2) => {
    return num1 / num2
}
console.log(divide(4, 3))

//Challenge 9 - Create a function that takes a number as a parameter and multiplies it by itself.
const multiplyItself = (num) => {
    return num * num
}
console.log(multiplyItself(4))